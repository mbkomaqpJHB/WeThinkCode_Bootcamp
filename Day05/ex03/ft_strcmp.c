#include <stdio.h>

int		ft_strcmp(char *s1, char *s2)
{
	unsigned int	i;

	i = 0;
	if (s1[i] > s2[i])
	{
		return (1);
	}
	else if (s1[i] < s2[i])
	{
		return (-1);
	}
	else
	{
		return (0);
	}
}
